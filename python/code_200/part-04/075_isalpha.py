#!/usr/bin/env python3
# -*-coding: utf-8 -*-


txt1 = 'A'
txt2 = '안녕'
txt3 = 'Python World'
txt4 = 'Dashim'

# isalpha()는 사람의 언어로마 구성되어 있는지 확인
# 띄어쓰기는 사람의 언어가 아님
ret1 = txt1.isalpha()
ret2 = txt2.isalpha()
ret3 = txt3.isalpha()
ret4 = txt4.isalpha()

print(ret1)
print(ret2)
print(ret3)
print(ret4)

#!/usr/bine/env python
# -*- coding: utf-8 -*-

# 정수 선언
int_data = 1                        
print(int_data)

# 실수 선언
float_data = 3.14                   
print(float_data)

# 복소수 선언
complex_data = 1+5j                 
print(complex_data)

# 문자열 선언(영문)
str_data1 = 'I love python'         
print(str_data1)

# 문자열 선언(한글)
str_data2 = '반갑습니다.'             
print(str_data2)

# 리스트 선언
# 리스트는 항상 [] 기호를 사용함
# 말그대로 자료들의 나열이고, 각 자료들 간에 구분은 콤마(,)로 하는 것임
# 아래는 '정수 리스트'임
list_data = [1, 2, 3]               
print(list_data)

# 튜플 선언
# 튜플은 항상 () 기호를 사용함
# 리스트와 비슷하지만, 가장 큰 사이는 Immutable임 즉 값을 변경할 수 없음
tuple_data = (1, 2, 3)              
print(tuple_data)

# 사전 선언
# 사전은 {} 기호를 사용함
# key와 value로 구성되면, key와 value는 ':'를 사용하여 쌍을 이룸
# 아래는 키는 0, 1이고, 밸류는 'False'와 'True'가 됨
dict_data = {0:'False', 1:'True'}   
print(dict_data)



#!/usr/bin/env python
# -*- coding: utf-8 -*-

# float 또는 real type을 실수형이라고 함

f1 = 1.0
print(f1)

f2 = 3.14
print(f2)

f3 = 1.56e3
print(f3)

f4 = -0.7e-4
print(f4)

print('---------------------------------------')

# 실수형과 정수형 연산은 모두 실수형임
i = 2
f = 4.0
print(f + i)
print(f * i)
print(f / i)

print('---------------------------------------')

# 나눗셈은 자료형과 상관없이 무조건 실수형임
print(4/2)